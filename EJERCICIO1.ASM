DATOS SEGMENT ; DEFINICIÓN DEL SEGMENTO DE DATOS
    A    DW 2
    B    DW 2
    C    DB 2
    D    DB 1
    X    DW ?
    SUMA DW ?
    REST DB ? 
    MULT DB ? 
    COCIENTE DB ? 
    
DATOS ENDS    ; FIN DEL SEGMENTO DE DATOS

PILA SEGMENT STACK "STACK" ; DEFINICIÓN DEL SEGMENTO DE PILA
PILA ENDS                  ; FIN DEL SEGMENTO DE PILA

CODE SEGMENT "CODE" ; DEFINICIÓN DEL SEGMENTO DE CODIGO
    ASSUME CS:CODE,DS:DATOS
    START PROC FAR  ; INICIO DEL PROCEDIMIENTO PRINCIPAL START
        MOV AX,DATOS
        MOV DS,AX

        ; SUMA (A+B)
        MOV AX,A
        MOV BX,B
        ADD AX,BX
        MOV SUMA,AX

        ; RESTA (C-D)
        MOV AL,C
        MOV AH,D
        SUB AL,AH
        MOV REST,AL

        ; DIVISION SUMA/RESTA
        MOV AX,SUMA
        MOV BL,REST
        DIV BL
        ;ADD AL,48
        MOV COCIENTE,AL

        ; MULTIPLICACIPON
        MOV AL,COCIENTE   ; MUEVE EL VALOR 2 AL REGISTRO AL
        MOV BL,2   ; MUEVE EL VALOR 2 AL REGISTRO BL 
        MUL BL     ; REALIZA LA MULTIPLICACIÓN 
        ADD AX,48  ; EL RESULTADO DE MUL SE GUARDA EN AX Y SE LE SUMA 48

        MOV DX,AX  ; MUEVE EL VALOR DE AX AL DX 
        MOV AH,02H ; INSTRUCCIÓN PARA COLOCAR EL CURSOR 
        INT 21H    ; PETICIÓN DE FUNCIÓN AL DOS
        
    START ENDP      ; FIN DEL PROCEDIMIENTO PRINCIPAL
CODE ENDS           ; FIN DEL SEGMENTO DE CODIGO 

END START           ; FIN DEL PROGRAMA
